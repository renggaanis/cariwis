						<p>
							Di Pantai Pulau Noko pengunjung dapat menikmati pantai berpasir putih dengan airnya yang jernih. Ombak di pantai ini pun tergolong tenang dan dilengkapi dengan deru angin yang menyejukkan.
						</p>
						<p>
