						<p>
							<b>Jakarta -</b> Menteri Desa, Pembangunan Daerah Tertinggal dan Transmigrasi, Abdul Halim Iskandar, meresmikan Wahana Edu Wisata Lontar Sewu di Desa Hendrosari, Kecamatan Menganti, Gresik, Jawa Timur. Peresmian ditandai dengan penandatangan prasasti Wahana Edu Wisata Lontar Sewu yang didampingi Bupati Gresik, Sambari Halim Radianto dan Wakil Bupati Gresik, Moh. Chosim.
						</p>
						<p>
							Abdul Halim mengapresiasi adanya gagasan yang dihasilkan dalam mengembangkan destinasi wisata di Desa Hendrosari dengan memanfaatkan potensi yang ada yakni potensi alam. Menurutnya, ada dua hal yang tidak akan pernah terdampak oleh krisis yakni wisata dan kuliner. Oleh karena itu, dirinya optimis bahwa desa yang memiliki dua hal tersebut akan tahan terhadap gejolak ekonomi.
						</p>
						<p>
							"Satu hal saja sudah tepat. Apalagi, Desa Hendrosari ini memiliki dua hal tersebut. Ini adalah suatu pilihan yang tepat. Jadi, wisata disini sangat luar biasa," kata Abdul Halim dalam keterangan tertulis, Senin (10/2/2020).
						</p>
						<p>
							Ia juga mengapresiasi Badan Usaha Milik Desa (BUMDes) Lontar Sewu yang terus berinovasi dalam mengembangkan usahanya.
						</p>
						<p>
							"Desa ini sudah memiliki BUMDes yang bagus dengan mengelola desa wisata. Apalagi, desa wisata dibangun atas dasar permasalahan dan potensi. Ini adalah suatu pendekatan yang sangat tepat," ungkapnya.
						</p>
						<p>
							Oleh karena itu, ia berpesan pada Dinas Pemberdayaan Masyarakat Desa untuk terus memotivasi dan memacu pendirian BUMDes yang tentunya harus betul-betul memahami kebutuhan dan potensi.
						</p>
						<p>
							Dalam peresmian destinasi wisata desa Hendrosari tersebut dirangkai dengan kegiatan minum legen (minuman hasil produksi dari air nira yang berasal dari pohon lontar) seribu gelas bersama Mendes yang diikuti oleh seluruh tamu undangan yang hadir dalam opening tersebut.
						</p>
						<p>
							Dalam opening ini, Bupati dan wakil Bupati Gresik, serta jajaran dari Pemerintah Kabupaten Gresik dan Forum Koordinasi Pimpinan Daerah (Forkopimda) Kabupaten Gresik turut mendampingi Abdul Halim.
						</p>
						<P>
							Wahana Edu Wisata Lontar Sewu menjadi salah satu tempat liburan alternatif bersama keluarga maupun teman. Tempat wisata yang ramah lingkungan ini memiliki keindahan pemandangan alam dengan hijaunya sawah dan pohon lontar yang bisa dinikmati oleh para pengunjung yang datang.
						</P>
						<p>
							Selain itu, tempat wisata yang dikelola oleh Badan Usaha Milik Desa (BUMDes) Lontar Sewu ini juga menawarkan pengunjung menikmati wisata air, arena bermain anak, taman rumah unik, jajanan kuliner, fasilitas area santai keluarga dan beberapa titik spot untuk ber-selfie di kawasan yang memiliki ribuan pohon lontar ini.
						</p>
						<p>
							Wisata baru di Kabupaten Gresik ini merupakan kerja sama Desa Hendrosari dengan Kemendes PDTT melalui Program Pilot Inkubasi Inovasi Desa Pengembangan Ekonomi Lokal (PIID-PEL) 2019.
						</p>
						<p>
							Program Pilot Inkubasi Inovasi Desa - Pengembangan Ekonomi Lokal yang dilaksanakan oleh Direktorat Pengembangan Usaha Ekonomi Desa (PUED) merupakan program/kegiatan fasilitasi yang dilakukan untuk mendorong pengembangan produk unggulan desa melalui kemitraan antara KUEMD. Termasuk di dalamnya ada koperasi, lembaga ekonomi desa (BUMDes), dan Pelaku Bisnis Profesional melalui konsep kemitraan yang dikenal dengan konsep kerjasama Pemerintah, Swasta, dan Masyarakat (Public-Private-People-Partnership).
						</p>
